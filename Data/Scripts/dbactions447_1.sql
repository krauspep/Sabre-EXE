ACTIVE BEFORE INSERT
POSITION 1
AS
begin
  if (new.CRIMINAL_RECORD_KEY is null)
  then begin
         new.CRIMINAL_RECORD_KEY = gen_id(CRIMINAL_RECORD_GEN, 1);
       end
end^

