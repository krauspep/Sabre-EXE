select RATE_OF_PAY
from EMPLOYEE_PAY_RATE_LINK
where EMPLOYEE_SHIFT_KEY in (select EMPLOYEE_SHIFT_KEY
from EMPLOYEE_SHIFT
where EMPLOYEE_KEY = 3
and SHIFT_KEY =7
and ACTIVE_RECORD = 1
and DELETED = 0)
and (EFFECTIVE_DATE_TO = '  /  /    ' or EFFECTIVE_DATE_TO = '/  /')
and ACTIVE_RECORD = 1
and DELETED = 0