select  A.ATTENDANCE_KEY,
        A.ATTENDANCE_TIME,
        A.ATTENDANCE_DATE,
        A.MANUAL_FIX,
        A.RECORD_STATUS,
        DL.DEVICE_LOCATION
from ATTENDANCE A
left outer join DEVICE_LOCATION DL on (DL.DEVICE_LOCATION_KEY = A.DEVICE_LOCATION_KEY)
where A.EMPLOYEE_KEY =:EMPLOYEE_KEY
and A.EVENT_KEY = 1
and A.ATTENDANCE_DATE between :FROM_DATE and :TO_DATE
and A.ACTIVE_RECORD = 1
and A.DELETED = 0
order by A.ATTENDANCE_DATE, A.ATTENDANCE_TIME