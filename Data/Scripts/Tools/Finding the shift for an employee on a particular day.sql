Select S.SHIFT_KEY,
S.FROM_TIME,
S.TO_TIME,
S.SPAN_DAY,
S.SHIFT_TYPE,
WS.ATTENDANCE_DATE_START,
WS.ATTENDANCE_DATE_END,
WS.SHIFT_KEY as WORK_SCHEDULE_SHIFT_KEY,
S.DEDUCT_MIN_TOTAL,
S.DEDUCT_MIN
From WORK_SCHEDULE WS
Left Outer Join SHIFT S on (S.SHIFT_KEY = WS.SHIFT_KEY)
Where WS.EMPLOYEE_KEY = 3
And WS.ATTENDANCE_DATE_START = '27.04.2011'
And WS.Active_Record = 1
And S.DELETED = 0
And WS.DELETED = 0
