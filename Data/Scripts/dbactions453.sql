CREATE GENERATOR VIP_COMPANY_GEN;

CREATE TABLE VIP_COMPANY
(
  VIP_COMPANY_KEY DM_KEY,
  VIP_COMPANY_CODE DM_CODE,
  VIP_COMPANY_NAME DM_NAME,
  DELETED DM_BOOLEAN DEFAULT 0,
  ACTIVE_RECORD DM_ACTIVE DEFAULT 1,
  PRIMARY KEY (VIP_COMPANY_KEY)
);

GRANT DELETE, INSERT, REFERENCES, SELECT, UPDATE ON VIP_COMPANY TO  "SYSDBA" WITH GRANT OPTION;

CREATE TRIGGER TR_VIP_COMPANY FOR VIP_COMPANY
ACTIVE BEFORE INSERT POSITION 0
AS
begin
  if (new.VIP_COMPANY_KEY is null)
  then begin
         new.VIP_COMPANY_KEY = gen_id(VIP_COMPANY_GEN, 1);
       end
end