ALTER TRIGGER CALC_ATTENDANCE_UPDATE ACTIVE
AFTER UPDATE POSITION 0
as begin 
    IF (old.IGNORETRIGGER = new.IGNORETRIGGER)
    then  DELETE FROM CALCULATED_TA WHERE (new.ATTENDANCE_DATE between FROM_DATE and TO_DATE) and new.EMPLOYEE_KEY = EMPLOYEE_KEY;
end