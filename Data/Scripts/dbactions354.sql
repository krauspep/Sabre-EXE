DROP TRIGGER TR_INSERT_OPERATOR;

CREATE TRIGGER TR_INSERT_OPERATOR FOR COMPANY ACTIVE BEFORE INSERT POSITION 0
AS
begin
  INSERT INTO OPERATOR(COMPANY_KEY, OPERATOR_NAME, OPERATOR_LOGIN_ID, OPERATOR_PASSWORD, USER_VISIBILITY, ACTIVE_RECORD, DELETED) VALUES(NEW.COMPANY_KEY, 'Administrator', 'Admin', 'admin', 'A', 1, 0);
end

