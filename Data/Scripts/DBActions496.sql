CREATE TABLE PAYROLL_EXPORT_TEMPLATE (
    TEMPLATE_KEY INT NOT NULL,
    TEMPLATE_NO  INT UNIQUE,
    TEMPLATE     VARCHAR(300),
    PRIMARY KEY( TEMPLATE_KEY )
)

GRANT DELETE, INSERT, REFERENCES, SELECT, UPDATE ON PAYROLL_EXPORT_TEMPLATE TO  "SYSDBA" WITH GRANT OPTION;

CREATE TRIGGER TR_PAYROLL_EXPORT_TEMPLATE FOR PAYROLL_EXPORT_TEMPLATE
ACTIVE BEFORE INSERT POSITION 0
AS
BEGIN
    IF ( new.TEMPLATE_KEY IS NULL ) THEN
    BEGIN
        new.TEMPLATE_KEY = GEN_ID( PAYROLL_EXPORT_TEMPLATE_GEN, 1 );
    END
END

