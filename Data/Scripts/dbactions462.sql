CREATE GENERATOR COURTS_GEN;

CREATE TABLE COURTS
(
  COURTS_KEY DM_KEY,
  COURTS DM_NAME,
  DELETED DM_BOOLEAN DEFAULT 0,
  ACTIVE_RECORD DM_ACTIVE DEFAULT 1,
  PRIMARY KEY (COURTS_KEY)
);

GRANT DELETE, INSERT, REFERENCES, SELECT, UPDATE ON COURTS TO  "SYSDBA" WITH GRANT OPTION;

CREATE TRIGGER TR_COURTS FOR COURTS
ACTIVE BEFORE INSERT POSITION 0
AS
begin
  if (new.COURTS_KEY is null)
  then begin
         new.COURTS_KEY = gen_id(COURTS_GEN, 1);
       end
end