CREATE GENERATOR COST_CENTER_GEN;

CREATE TABLE COST_CENTER
(
  COST_CENTER_KEY DM_KEY,
  COST_CENTER_NAME DM_NAME,
  DELETED DM_BOOLEAN DEFAULT 0,
  ACTIVE_RECORD DM_ACTIVE DEFAULT 1,
  PRIMARY KEY (COST_CENTER_KEY)
);

GRANT DELETE, INSERT, REFERENCES, SELECT, UPDATE ON COST_CENTER TO  "SYSDBA" WITH GRANT OPTION;

CREATE TRIGGER TR_COST_CENTER FOR COST_CENTER BEFORE INSERT POSITION 0
AS
begin
  if (new.COST_CENTER_KEY is null)
  then begin
         new.COST_CENTER_KEY = gen_id(COST_CENTER_GEN, 1);
       end
end
