ALTER TABLE EMPLOYEE ADD RELIGION_KEY DM_KEY_N1;

ALTER TABLE EMPLOYEE ADD LANGUAGE_KEY DM_KEY_N1;

CREATE GENERATOR LANGUAGE_GEN;

CREATE TABLE LANGUAGE
(
  LANGUAGE_KEY DM_KEY,
  LANGUAGE_NAME VARCHAR(60),
  DELETED DM_BOOLEAN DEFAULT 0,
  ACTIVE_RECORD DM_ACTIVE DEFAULT 1,
  PRIMARY KEY (LANGUAGE_KEY)
);

GRANT DELETE, INSERT, REFERENCES, SELECT, UPDATE
 ON LANGUAGE TO  SYSDBA WITH GRANT OPTION;

CREATE TRIGGER TR_LANGUAGE FOR LANGUAGE ACTIVE
BEFORE INSERT POSITION 0
AS
begin
  if (new.LANGUAGE_KEY is null)
  then begin
         new.LANGUAGE_KEY = gen_id(LANGUAGE_GEN, 1);
       end
end

CREATE TRIGGER TR_LANGUAGE_VALUES FOR LANGUAGE ACTIVE
BEFORE INSERT POSITION 0
AS
begin
 new.DELETED = 0;
 new.ACTIVE_RECORD = 1;
end
