CREATE GENERATOR COST_CENTER_LINKED_DEPT_GEN;

CREATE TABLE COST_CENTER_LINKED_DEPT
(
  COST_CENTER_LINKED_DEPT_KEY DM_KEY NOT NULL,
  COST_CENTER_KEY DM_KEY_N1,
  DEPARTMENT_KEY DM_KEY_N1,
  DELETED DM_BOOLEAN DEFAULT 0,
  ACTIVE_RECORD DM_ACTIVE DEFAULT 1,
  PRIMARY KEY (COST_CENTER_LINKED_DEPT_KEY)
);

GRANT DELETE, INSERT, REFERENCES, SELECT, UPDATE ON COST_CENTER_LINKED_DEPT TO SYSDBA WITH GRANT OPTION;

CREATE TRIGGER TR_COST_CENTER_LINKED_DEPT FOR COST_CENTER_LINKED_DEPT ACTIVE BEFORE INSERT POSITION 0
AS
begin
  if (new.COST_CENTER_LINKED_DEPT_KEY is null)
  then begin
         new.COST_CENTER_LINKED_DEPT_KEY = gen_id(COST_CENTER_LINKED_DEPT_GEN, 1);
       end
end





