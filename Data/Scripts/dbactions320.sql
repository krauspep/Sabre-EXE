ALTER TABLE COMPANY ADD LPR_LOG_FILE DM_NAME_NULL;

CREATE GENERATOR CAMERA_MAKE_GEN;

CREATE TABLE CAMERA_MAKE
(
  CAMERA_MAKE_KEY DM_KEY,
  CAMERA_MAKE DM_VARCHAR_20,
  ACTIVE_RECORD DM_ACTIVE DEFAULT 1,
  DELETED DM_BOOLEAN DEFAULT 0,
  PRIMARY KEY (CAMERA_MAKE_KEY)
);


GRANT DELETE, INSERT, REFERENCES, SELECT, UPDATE ON CAMERA_MAKE TO SYSDBA WITH GRANT OPTION;

CREATE TRIGGER TR_CAMERA_MAKE FOR CAMERA_MAKE ACTIVE BEFORE INSERT POSITION 0
AS
begin
  if (new.CAMERA_MAKE_KEY is null)
  then begin
         new.CAMERA_MAKE_KEY = gen_id(CAMERA_MAKE_GEN, 1);
       end
end


