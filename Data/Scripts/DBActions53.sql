CREATE GENERATOR BIO_PC_CONFIGURATION_GEN;
COMMIT;

CREATE TABLE "BIO_PC_CONFIGURATION"
(
"BIO_PC_CONFIGURATION_KEY"       "DM_KEY",
"BIO_PC_CODE"                    "DM_CODE",
"COMPANY_KEY"                    "DM_KEY",
"TERMINAL_KEY"                   "DM_KEY",
"DEVICE_LOCATION_KEY"            "DM_KEY",
"DEPARTMENT_KEY"                 "DM_KEY",
"BIO_PC_DESCRIPTION"             "DM_BLOB2",
"BIO_PC_FUNCTION"                "DM_SMALLINT",
"DELETED"                        "DM_BOOLEAN",
PRIMARY KEY ("BIO_PC_CONFIGURATION_KEY")
);

SET TERM ^ ;
CREATE TRIGGER TR_BIO_PC_CONFIGURATION FOR BIO_PC_CONFIGURATION
ACTIVE BEFORE INSERT POSITION 0
AS
begin
  if (new.BIO_PC_CONFIGURATION_KEY is null)
  then begin
         new.BIO_PC_CONFIGURATION_KEY = gen_id(BIO_PC_CONFIGURATION_GEN, 1);
       end
end
^
SET TERM ; ^

COMMIT;


CREATE TABLE "BIO_PC_ACCESS"
(
"BIO_PC_CONFIGURATION_KEY"       "DM_KEY",
"ENROLL_ID"                       "DM_INTEGER",
"ADMINISTRATOR"                  "DM_BOOLEAN",
"DELETED"                        "DM_BOOLEAN"
);

COMMIT;