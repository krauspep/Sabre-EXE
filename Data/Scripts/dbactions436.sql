CREATE GENERATOR INDIVIDUAL_TYPE_GEN;

CREATE TABLE INDIVIDUAL_TYPE
(
  INDIVIDUAL_TYPE_KEY DM_KEY,
  COMPANY_KEY DM_KEY,
  INDIVIDUAL_TYPE_NAME DM_NAME,
  DELETED DM_BOOLEAN DEFAULT 0,
  ACTIVE_RECORD DM_ACTIVE DEFAULT 1,
  PRIMARY KEY (INDIVIDUAL_TYPE_KEY)
);

GRANT DELETE, INSERT, REFERENCES, SELECT, UPDATE ON INDIVIDUAL_TYPE TO  "SYSDBA" WITH GRANT OPTION;

CREATE TRIGGER TR_INDIVIDUAL_TYPE FOR INDIVIDUAL_TYPE
ACTIVE BEFORE INSERT POSITION 0
AS
begin
  if (new.INDIVIDUAL_TYPE_KEY is null)
  then begin
         new.INDIVIDUAL_TYPE_KEY = gen_id(INDIVIDUAL_TYPE_GEN, 1);
       end
end
