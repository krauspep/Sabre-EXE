CREATE TABLE IMPORT_RESIDENT_DATA
(
  ENROLL_ID INTEGER,
  RESIDENT_NO SMALLINT,
  UNIT_TYPE VARCHAR(30),
  PROPERTY_NO VARCHAR(30),
  RESIDENT_STATUS_NAME VARCHAR(80),
  TITLE_NAME VARCHAR(30),
  RESIDENT_FIRST_NAME DM_NAME_NULL,
  RESIDENT_SURNAME DM_NAME_NULL,
  ID_NO DM_NAME_NULL,
  DATE_OCCUPIED DATE,
  DELETED DM_BOOLEAN DEFAULT 0,
  ACTIVE_RECORD DM_ACTIVE DEFAULT 1
);

GRANT DELETE, INSERT, REFERENCES, SELECT, UPDATE
ON IMPORT_RESIDENT_DATA TO  SYSDBA WITH GRANT OPTION;

CREATE TRIGGER TR_IMPORT_RES_DATA_VALUES FOR IMPORT_RESIDENT_DATA ACTIVE
BEFORE INSERT POSITION 0
AS
begin
 new.DELETED = 0;
 new.ACTIVE_RECORD = 1;
end


