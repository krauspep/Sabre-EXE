CREATE GENERATOR INDIVIDUAL_LOCATION_GEN;

CREATE TABLE INDIVIDUAL_LOCATION
(
  INDIVIDUAL_LOCATION_KEY DM_KEY,
  COMPANY_KEY DM_KEY NOT NULL,
  INDIVIDUAL_LOCATION_NAME DM_NAME,
  DELETED DM_BOOLEAN DEFAULT 0,
  ACTIVE_RECORD DM_ACTIVE DEFAULT 1,
  SITE_KEY DM_KEY,
  PRIMARY KEY (INDIVIDUAL_LOCATION_KEY)
);

GRANT DELETE, INSERT, REFERENCES, SELECT, UPDATE ON INDIVIDUAL_LOCATION TO  "SYSDBA" WITH GRANT OPTION;

CREATE TRIGGER TR_INDIVIDUAL_LOCATION FOR INDIVIDUAL_LOCATION BEFORE INSERT POSITION 0
AS
begin
  if (new.INDIVIDUAL_LOCATION_KEY is null)
  then begin
         new.INDIVIDUAL_LOCATION_KEY = gen_id(INDIVIDUAL_LOCATION_GEN, 1);
       end
end