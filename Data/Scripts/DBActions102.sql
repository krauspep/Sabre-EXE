CREATE TABLE TITLE 
(
  TITLE_KEY          DM_KEY,
  TITLE_NAME         DM_MODEL,
  DELETED            DM_BOOLEAN,
  PRIMARY KEY (TITLE_KEY)
);

CREATE TRIGGER "TR_TITLE" FOR "TITLE" ACTIVE BEFORE INSERT POSITION 0 AS begin if (new.TITLE_KEY is null) then begin new.TITLE_KEY = gen_id(TITLE_GEN, 1); end end

