CREATE GENERATOR ACCSYS_COMPANY_GEN;

CREATE TABLE ACCSYS_COMPANY
(
  ACCSYS_COMPANY_KEY DM_KEY,
  ACCSYS_COMPANY_CODE DM_CODE,
  ACCSYS_COMPANY_NAME DM_NAME,
  DELETED DM_BOOLEAN DEFAULT 0,
  ACTIVE_RECORD DM_ACTIVE DEFAULT 1,
  PRIMARY KEY (ACCSYS_COMPANY_KEY)
);

GRANT DELETE, INSERT, REFERENCES, SELECT, UPDATE ON ACCSYS_COMPANY TO  "SYSDBA" WITH GRANT OPTION;

CREATE TRIGGER TR_ACCSYS_COMPANY FOR ACCSYS_COMPANY
ACTIVE BEFORE INSERT POSITION 0
AS
begin
  if (new.ACCSYS_COMPANY_KEY is null)
  then begin
         new.ACCSYS_COMPANY_KEY = gen_id(ACCSYS_COMPANY_GEN, 1);
       end
end