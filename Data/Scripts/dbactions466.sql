CREATE GENERATOR STATE_OF_ORIGIN_GEN;

CREATE TABLE STATE_OF_ORIGIN
(
  STATE_OF_ORIGIN_KEY DM_KEY,
  STATE_OF_ORIGIN_NAME DM_NAME,
  DELETED DM_BOOLEAN DEFAULT 0,
  ACTIVE_RECORD DM_ACTIVE DEFAULT 1,
  PRIMARY KEY (STATE_OF_ORIGIN_KEY)
);

GRANT DELETE, INSERT, REFERENCES, SELECT, UPDATE ON STATE_OF_ORIGIN TO  "SYSDBA" WITH GRANT OPTION;

CREATE TRIGGER TR_STATE_OF_ORIGIN FOR STATE_OF_ORIGIN
ACTIVE BEFORE INSERT POSITION 0
AS
begin
  if (new.STATE_OF_ORIGIN_KEY is null)
  then begin
         new.STATE_OF_ORIGIN_KEY = gen_id(STATE_OF_ORIGIN_GEN, 1);
       end
end

